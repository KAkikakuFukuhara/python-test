class bcolors:
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKCYAN = '\033[96m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'

print(f"header{bcolors.HEADER}")
print(f"okblue{bcolors.OKBLUE}")
print(f"okcyan{bcolors.OKCYAN}")
print(f"okgreen{bcolors.OKGREEN}")
print(f"warning{bcolors.WARNING}")
print(f"fail{bcolors.FAIL}")
print(f"endc{bcolors.ENDC}")
print(f"bold{bcolors.BOLD}")
print(f"underline{bcolors.UNDERLINE}")


