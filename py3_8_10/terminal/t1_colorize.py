class bcolors:
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKCYAN = '\033[96m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'

print("header" + bcolors.HEADER)
print("okblue" + bcolors.OKBLUE)
print("okcyan" + bcolors.OKCYAN)
print("okgreen" + bcolors.OKGREEN)
print("warning" + bcolors.WARNING)
print("fail" + bcolors.FAIL)
print("endc" + bcolors.ENDC)
print("bold" + bcolors.BOLD)
print("underline" + bcolors.UNDERLINE)


